static UWORD GetRandom(WATERTAB *w, int n)
{
	int i;
	UWORD random;

	do {
		random = rand() & 0xfc;

		for (i = 0; i<n; i++)
		{
			if (w[i].random == random)
				break;
		}

		if (i == n)
			return random;

	} while (1);

	return 0;		// to stop warning
}

